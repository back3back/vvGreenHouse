    public static int[] calculateCRC16(byte[] data, int offset, int len) {
        int[] crc = { 0xFF, 0xFF };
        int uIndex;
        for (int i = offset; i < len && i < data.length; i++) {
            int nextByte = 0xFF & ((int) data[i]);
            uIndex = crc[0] ^ nextByte;
            crc[0] = crc[1] ^ auchCRCHi[uIndex];
            crc[1] = auchCRCLo[uIndex];
        }
        return crc;
    }

    /** 验证 CRC16 */
    public static boolean isCRCValid(byte[] buff) {
        int[] crc = calculateCRC16(buff, 0, buff.length - 2);
        return (byte) crc[0] == buff[buff.length - 2]
            && (byte) crc[1] == buff[buff.length - 1];
    }

    /** Hex字符串 "XX XX XX" → byte[] */
    public static byte[] hexToBytes(String hex) {
        String clean = hex.replaceAll(" ", "").trim();
        byte[] ret = new byte[clean.length() / 2];
        byte[] tmp = clean.getBytes();
        for (int i = 0; i < clean.length() / 2; i++) {
            ret[i] = uniteBytes(tmp[i * 2], tmp[i * 2 + 1]);
        }
        return ret;
    }

    private static byte uniteBytes(byte src0, byte src1) {
        byte _b0 = Byte.decode("0x" + new String(new byte[]{src0})).byteValue();
        _b0 = (byte) (_b0 << 4);
        byte _b1 = Byte.decode("0x" + new String(new byte[]{src1})).byteValue();
        return (byte) (_b0 ^ _b1);
    }

    /** uint16 大端 → int */
    public static int readU16(byte[] buf, int off) {
        return ((buf[off] & 0xFF) << 8) | (buf[off + 1] & 0xFF);
    }

    /** uint16 大端 → float, 除以10 */
    public static float readU16Div10(byte[] buf, int off) {
        return readU16(buf, off) / 10.0f;
    }
