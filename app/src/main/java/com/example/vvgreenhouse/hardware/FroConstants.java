    /**
     * 将不含CRC的hex前缀计算出完整命令（追加CRC16）
     * 例如: "010300140002" → 发送用完整命令
     */
    public static String buildCmdWithCRC(String prefixWithSpace) {
        String clean = prefixWithSpace.replaceAll(" ", "").trim();
        if (clean.length() % 2 != 0) clean = "0" + clean;
        int byteLen = clean.length() / 2;
        byte[] buf = new byte[byteLen];
        for (int i = 0; i < byteLen; i++) {
            buf[i] = (byte) Integer.parseInt(clean.substring(i * 2, i * 2 + 2), 16);
        }
        int[] crc = calculateCRC16(buf, 0, buf.length);
        return prefixWithSpace.trim() + " "
                + String.format("%02x", (byte) crc[0]).toLowerCase()
                + " " + String.format("%02x", (byte) crc[1]).toLowerCase();
    }

